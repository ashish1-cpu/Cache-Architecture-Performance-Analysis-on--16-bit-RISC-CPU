`timescale 1ns/1ps

module Instruction_Memory
(
    input  [15:0] pc,
    input  [1:0]  pattern_select,
    output [15:0] instruction
);

reg [15:0] memory [0:14];

wire [3:0] rom_addr;

assign rom_addr = pc[4:1];

always @(*)
begin

    //--------------------------------------------------
    // PATTERN 0 : TEMPORAL LOCALITY
    // Address sequence:
    // 5 5 5 5 5 5 5 ...
    //--------------------------------------------------

    if(pattern_select == 2'b00)
    begin

        memory[0]  = 16'b0000000001000101;
        memory[1]  = 16'b0000000001000101;
        memory[2]  = 16'b0000000001000101;
        memory[3]  = 16'b0000000001000101;
        memory[4]  = 16'b0000000001000101;
        memory[5]  = 16'b0000000001000101;
        memory[6]  = 16'b0000000001000101;
        memory[7]  = 16'b0000000001000101;

        memory[8]  = 16'b0000000001000101;
        memory[9]  = 16'b0000000001000101;
        memory[10] = 16'b0000000001000101;
        memory[11] = 16'b0000000001000101;
        memory[12] = 16'b0000000001000101;
        memory[13] = 16'b0000000001000101;
        memory[14] = 16'b0000000001000101;

    end

    //--------------------------------------------------
    // PATTERN 1 : SPATIAL LOCALITY
    // Address sequence:
    // 0 1 2 3 4 5 6 7 ...
    //--------------------------------------------------

    else if(pattern_select == 2'b01)
    begin

        memory[0]  = 16'b0000000001000000; //0
        memory[1]  = 16'b0000000001000001; //1
        memory[2]  = 16'b0000000001000010; //2
        memory[3]  = 16'b0000000001000011; //3
        memory[4]  = 16'b0000000001000100; //4
        memory[5]  = 16'b0000000001000101; //5
        memory[6]  = 16'b0000000001000110; //6
        memory[7]  = 16'b0000000001000111; //7

        memory[8]  = 16'b0000000001001000; //8
        memory[9]  = 16'b0000000001001001; //9
        memory[10] = 16'b0000000001001010; //10
        memory[11] = 16'b0000000001001011; //11
        memory[12] = 16'b0000000001001100; //12
        memory[13] = 16'b0000000001001101; //13
        memory[14] = 16'b0000000001001110; //14

    end

    //--------------------------------------------------
    // PATTERN 2 : CONFLICT MISSES
    // Address sequence:
    // 0 8 16 24 0 8 16 24 ...
    //--------------------------------------------------

    else if(pattern_select == 2'b10)
    begin

        memory[0]  = 16'b0000000001000000; //0
        memory[1]  = 16'b0000000001001000; //8
        memory[2]  = 16'b0000000001000000; //0
        memory[3]  = 16'b0000000001001000; //8

              memory[4]  = 16'b0000000001000000; //0
        memory[5]  = 16'b0000000001001000; //8
        memory[6]  = 16'b0000000001000000; //0
        memory[7]  = 16'b0000000001001000; //8

             memory[8]  = 16'b0000000001000000; //0
        memory[9]  = 16'b0000000001001000; //8
        memory[10]  = 16'b0000000001000000; //0
        memory[11]  = 16'b0000000001001000; //8

            memory[12]  = 16'b0000000001000000; //0
        memory[13]  = 16'b0000000001001000; //8
        memory[14]  = 16'b0000000001000000; //0
      

    end

    //--------------------------------------------------
    // PATTERN 3 : RANDOM
    // Address sequence:
    // 0 13 26 39 52 1 14 27 ...
    //--------------------------------------------------

    else
    begin

        memory[0]  = 16'b0000000001000000; //0
        memory[1]  = 16'b0000000001001101; //13
        memory[2]  = 16'b0000000001011010; //26
        memory[3]  = 16'b0000000001100111; //39
        memory[4]  = 16'b0000000001110100; //52

        memory[5]  = 16'b0000000001000001; //1
        memory[6]  = 16'b0000000001001110; //14
        memory[7]  = 16'b0000000001011011; //27
        memory[8]  = 16'b0000000001101000; //40
        memory[9]  = 16'b0000000001110101; //53

        memory[10] = 16'b0000000001000010; //2
        memory[11] = 16'b0000000001001111; //15
        memory[12] = 16'b0000000001011100; //28
        memory[13] = 16'b0000000001101001; //41
        memory[14] = 16'b0000000001110110; //54

    end

end

assign instruction = memory[rom_addr];

endmodule